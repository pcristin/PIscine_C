int	ft_sqrt(int nb)
{
	int a;

	a = 0;
	while ((a * a) != nb && a < (nb / 2) + 1)
	{
		a = a + 1;
	}
	if (a * a != nb || nb < 0)
	{
		return (0);
	}
	return (a);
}

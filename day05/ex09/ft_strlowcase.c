char	*ft_strlowcase(char *str)
{
	char *str_temp;

	str_temp = str;
	while (*str != '\0')
	{
		if (*str >= 65 && *str <= 90)
		{
			*str += 32;
			str++;
		}
		else
			str++;
	}
	return (str_temp);
}

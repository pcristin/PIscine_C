char	*ft_strcpy(char *dest, char *src)
{
	char *dest_tmp;

	dest_tmp = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (dest_tmp);
}

void	ft_print_comb(void)
{
	char a[3];

	a[0] = '0' - 1;
	a[1] = '0';
	a[2] = '1';
	while (++a[0] <= '7')
	{
		while (++a[1] <= '8')
		{
			while (++a[2] <= '9')
			{
				ft_putchar(a[0]);
				ft_putchar(a[1]);
				ft_putchar(a[2]);
				if (a[0] < '7' || a[1] < '8' || a[2] < '9')
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
			}
			a[2] = a[1] + 1;
		}
		a[1] = a[0] + 1;
		a[2] = a[1] + 1;
	}
}
